#include <stdio.h>

int main() {
	
    int num, digit, i = 0;
    
    scanf("%d", &num);

    while (num > 0) {
		
        digit = num % 10;			// Получаем последнюю цифру
        num /= 10;					// Удаляем последнюю цифру из числа
        if (digit % 2 != 0) i = ++i;//Проверка четности взятой цифры 	        
    }								//с флагом нечетности	
    
    (i == 0) ? puts("YES") : puts("NO") ;//Проверка счетчика 
	
	return 0;
}

